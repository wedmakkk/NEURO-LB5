    private void jButtonCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalculateActionPerformed
        int selected = jTable2.getSelectedRow();
    if (selected == -1) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Выберите строку для вычисления.",
            "Предупреждение", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    try {
        double lower = Double.parseDouble(model.getValueAt(selected, 0).toString());
        double upper = Double.parseDouble(model.getValueAt(selected, 1).toString());
        double step = Double.parseDouble(model.getValueAt(selected, 2).toString());

        double result = integrateSqrt(step, lower, upper);
        model.setValueAt(String.format("%.4f", result), selected, 3);

    } catch (NumberFormatException | NullPointerException e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Ошибка в данных строки.",
            "Ошибка", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (IllegalArgumentException e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            e.getMessage(),
            "Ошибка", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButtonCalculateActionPerformed

