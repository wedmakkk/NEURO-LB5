    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        try {
        // Читаем текст из полей ввода
            String lowerStr = jTextField1.getText().trim();
            String upperStr = jTextField2.getText().trim();
            String stepStr = jTextField3.getText().trim();

        // Преобразуем в числа
            double lower = Double.parseDouble(lowerStr);
            double upper = Double.parseDouble(upperStr);
            double step = Double.parseDouble(stepStr);

        // Проверяем, что шаг положительный
            if (step <= 0) {
                JOptionPane.showMessageDialog(this,
                "Шаг должен быть положительным числом.",
                "Ошибка ввода", JOptionPane.ERROR_MESSAGE);
                return;
        }

            // Получаем модель таблицы и добавляем строку
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.addRow(new Object[]{lower, upper, step, ""});

            // Очищаем поля ввода (опционально)
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
            "Пожалуйста, введите корректные числа (разделитель дробной части — точка).",
            "Ошибка ввода", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

